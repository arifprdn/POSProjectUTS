    private void BayarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarButtonActionPerformed
            // TODO add your handling code here:
            Kembalian();
            String timeStamp = new SimpleDateFormat("yyyy.MM.dd.HH.mm.ss").format(new java.util.Date());
            Transaksi.daftarBelanja.add(new String[] {daftarBelanja,TotalTextField.getText(),BayarTextField.getText(),KembalianTextField.getText(),timeStamp});
    try {    
    try {
            // Buat pernyataan SQL untuk memasukkan data transaksi ke dalam database
            //Statement stmt=DBconnector.connection.createStatement();
            String sql = "INSERT INTO transaksi (daftar_belanja, total_belanja, jumlah_bayar,kembalian) VALUES (?, ?, ?, ?)";
            PreparedStatement statement = DBconnector.connection.prepareStatement(sql);
            
            statement.setString(1, Transaksi.daftarBelanja.get(indexofTransaksi)[0]);
            statement.setInt(2, Integer.valueOf(Transaksi.daftarBelanja.get(indexofTransaksi)[1]));
            statement.setInt(3, Integer.valueOf(Transaksi.daftarBelanja.get(indexofTransaksi)[2]));
            statement.setInt(4, Integer.valueOf(Transaksi.daftarBelanja.get(indexofTransaksi)[3]));
            statement.executeUpdate();
            indexofTransaksi = indexofTransaksi + 1;
            
           
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
    }catch (NumberFormatException e){      
                JOptionPane.showMessageDialog(rootPane, "Uang tidak cukup!");       
    }
    
    
    //DELETE SEMUA DATA DI FORM YANG SUDAH DI TRANSFER KE ARRAYLIST DAN JUGA DATABASE
    DefaultTableModel model = (DefaultTableModel) TabelBarang.getModel();
    model.setRowCount(0);
    TotalTextField.setText("");
    KembalianTextField.setText("");
    BayarTextField.setText("0");
    sum = 0;
    
    
    
    }//GEN-LAST:event_BayarButtonActionPerformed

