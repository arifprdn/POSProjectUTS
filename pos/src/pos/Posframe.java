    public void Kembalian(){
        int total=Integer.parseInt(TotalTextField.getText());
        int bayar=Integer.parseInt(BayarTextField.getText());
        
        int balek=bayar-total;
        if(bayar>total){
            KembalianTextField.setText(String.valueOf(balek));
        }
        else if(bayar<total){
        KembalianTextField.setText("Uang anda Tidak Cukup");
        }
        else{
            KembalianTextField.setText("0");
        }
    }
